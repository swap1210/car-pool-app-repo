import UIKit

struct Trip{
    var destination = ""
    var requester = ""
}

class DriverDashboardTableVC: UITableViewController {

    var tripArray: [Trip] = []
    
    override func viewDidLoad() {
        super.viewDidLoad()
        
        self.title = "Driver Dashboard"
        
        populateTrips()

        // Uncomment the following line to preserve selection between presentations
